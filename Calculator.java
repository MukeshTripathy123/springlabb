package com.calculator.clac;

public class Calculator {
public static int addNo(int num1,int num2)
{
return num1+num2;
}

public static int subNo(int num1,int num2)
{
	return num1-num2;
}
public static double multiNo(double num1,double num2)
{
	return num1*num2;
}
public static double divNo(double num1,double num2)
{
	
	return num1/num2;    }
}
