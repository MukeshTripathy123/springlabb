package com.calculator.clac;

public class Calculator {
public static int addNo(int num1,int num2)
{
return num1+num2;
}

public static int subNo(int x,int y)
{
	return x-y;
}
public static double multiNo(double x,double y)
{
	return x*y;
}
public static double divNo(double x,double y)
{
	
	return x/y;    }
}
